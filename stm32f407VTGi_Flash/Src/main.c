/*
 * Store User data
 * */
struct led_t {
	uint8_t cnt;  	//count up every time a user button is pressed.
	uint8_t led1;	//not being used
	uint8_t led2;	//not being used
	uint8_t led3;	//not being used
	uint8_t led4;	//not being used
};

/*
 * 	Hold the flash led instance.
 * */
static struct flash_t flash_user_data;

/*
 * used for not updating the led if nothing changes
 * */
static uint8_t led_change=0xFFU;
/*
 * initialized the led data.
 */
static struct led_t led = { 0x00, 0x00, 0x00, 0x00, 0x00 };
/*
 * if a led cnt is changed then turn on/off the led.
 */
static void update_led(void) {
	if (led.cnt != led_change) { //avoid keep writing to the leds.
		if (led.cnt == 0) {
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_RESET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_RESET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_RESET);
		} else if (led.cnt == 1) {
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_SET);
		} else if (led.cnt == 2) {
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET);
		} else if (led.cnt == 3) {
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);
		} else if (led.cnt == 4) {
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_13, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_15, GPIO_PIN_SET);
		}
		led_change = led.cnt;
	}
}

/*
 * This task is called every 200ms to update the leds.
 * if anything changes then update the flash
 */
void flash_update(void *arg) {
	while (true) {
		update_led();
		flash_user_data.flash_update(&flash_user_data);
		osDelay(200);
	}
}

/*
 * Interrupted when user press the user button.
 * increment the cnt.
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if (GPIO_Pin == GPIO_PIN_0) {
		led.cnt = (led.cnt+1) % 5;
	}
}
